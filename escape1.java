package unit3;

public class escape1 {
  public static void main(String[] args) {
    System.out.println("hello world");

    System.out.println("Hello \"KgCoding\"");// here \ will helps to print with double inverted comma

    System.out.println("hello\tKgCoding"); //\t gives a tab space or use 4 times spacebar key

    System.out.println("hello\bKgCoding");// will insert backspace

    System.out.println("Hello \nKgCoding");// after \n new line 

    System.out.println("Hello \'kGCoding\'"); // will print with single cotes

    System.out.println("hello \\kgcoding\\");// will print backslash
  }
}
